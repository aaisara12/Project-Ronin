using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class MoveState : StateMachineBehaviour
{
    CharacterCaptureController movementController = null;

    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        movementController = animator.GetComponent<CharacterCaptureController>();

        if (movementController == null)
        {
            throw new MissingComponentException("Animator State Error: Characterr doesn't have CharacterCaptureController component!");
        }
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        Vector3 movement = new Vector3(animator.GetFloat("xInput"), 0, animator.GetFloat("yInput"));

        movementController.MoveInDirection(movement);
    }

